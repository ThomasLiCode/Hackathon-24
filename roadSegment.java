public class roadSegment {
    private int trafficLevel;
    private int exitTime;

    public roadSegment(int trafficLevelR){
        this.trafficLevel = trafficLevelR;
        this.exitTime = exitTime;
    }

    public int trafficLevel(){
        return trafficLevel;
    }

    public int exitTime(){
        return exitTime;
    }

}
